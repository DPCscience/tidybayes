#' Point estimate + multiple probability interval geoms for ggplot
#'
#' Modified versions of \code{\link{geom_pointrange}} and \code{\link{geom_pointrangeh}} with default aesthetics
#' designed for use with output from \code{\link{point_interval}}.
#'
#' \code{geom_pointinterval} is a modified version of \code{\link{geom_pointrange}}, and \code{geom_pointintervalh} is
#' a modified version of \code{\link{geom_pointrangeh}}. These geoms set some default aesthetics equal
#' to the \code{conf.low}, \code{conf.high}, and \code{.prob} columns generated by the \code{point_interval} family
#' of functions, making them often more convenient than vanilla \code{\link{geom_pointrange}} or
#' \code{\link{geom_pointrangeh}} when used with functions like \code{\link{mean_qi}}, \code{\link{median_qi}}, etc.
#'
#' Specifically, \code{geom_pointinterval} acts as if its default aesthetics are
#' \code{aes(ymin = conf.low, ymax = conf.high, size = -.prob)}. \code{geom_pointintervalh} acts as if its default
#' aesthetics are \code{aes(xmin = conf.low, xmax = conf.high, size = -.prob)}.
#'
#' Both geoms provides a scaling factor for line width as well as point size through the \code{fatten.interval} and
#' \code{fatten.point} arguments; this scaling factor is designed to give multiple probability intervals reasonable
#' scaling at the default settings for \code{\link{scale_size_continuous}}. Finally, these geoms default to not
#' displaying the legend, though this can be overriden through setting \code{show.legend = NA} (the setting for most
#' geoms) or \code{show.legend = TRUE}.
#'
#' @param mapping The aesthetic mapping, usually constructed with
#' \code{\link{aes}} or \code{\link{aes_string}}. Only needs to be set at the
#' layer level if you are overriding the plot defaults.
#' @param data A layer specific dataset - only needed if you want to override
#' the plot defaults.
#' @param stat The statistical transformation to use on the data for this layer.
#' @param position The position adjustment to use for overlapping points on this layer.
#' @param ...  Other arguments passed to \code{\link{layer}}.
#' @param fatten.interval A multiplicative factor used to adjust the size of the interval
#' lines (line size will be \code{(size + 3) * fatten.interval}. The default decreases the line size, because the
#' default range of \code{\link{scale_size_continuous}} has an upper end of 6, which is quite large.
#' @param fatten.point A multiplicate factor used to adjust the size of the point relative to the largest line.
#' @param na.rm	If \code{FALSE}, the default, missing values are removed with a warning. If \code{TRUE}, missing
#' values are silently removed.
#' @param show.legend Should this later be included in the legends? Default is \code{FALSE}, unlike most geoms,
#' to match its common use cases; N.B. this is unlike most geoms, which have a default of \code{NA}.
#' @param inherit.aes If \code{FALSE}, overrides the default aesthetics, rather than combining with them. This is
#' most useful for helper functions that define both data and aesthetics and shouldn't inherit behaviour from the
#' default plot specification, e.g. borders.
#' @author Matthew Kay
#' @seealso See \code{\link{geom_lineribbon}} for a similar geom designed for curves plus probability bands. See
#' \code{\link{geom_pointrange}} and \code{\link{geom_pointrangeh}} for the geoms these are based on.
#' @keywords manip
#' @examples
#'
#' library(magrittr)
#' library(ggplot2)
#' data(RankCorr)
#'
#' RankCorr %>%
#'   spread_samples(u_tau[i]) %>%
#'   mean_qi(.prob = c(.8, .95)) %>%
#'   ggplot(aes(y = i, x = u_tau)) +
#'   geom_pointintervalh()
#'
#' RankCorr %>%
#'   spread_samples(u_tau[i]) %>%
#'   mean_qi(.prob = c(.8, .95)) %>%
#'   ggplot(aes(x = i, y = u_tau)) +
#'   geom_pointinterval()
#'
#' @importFrom ggstance geom_linerangeh GeomLinerangeh
#' @import ggplot2
#' @export
geom_pointinterval <- function(mapping = NULL, data = NULL,
  stat = "identity", position = "identity",
  ...,
  fatten.interval = 0.15,
  fatten.point = 1.8,
  na.rm = FALSE,
  show.legend = FALSE,
  inherit.aes = TRUE) {

  l = layer(
    data = data,
    mapping = mapping,
    stat = stat,
    geom = GeomPointinterval,
    position = position,
    show.legend = show.legend,
    inherit.aes = inherit.aes,
    params = list(
      fatten.point = fatten.point,
      fatten.interval = fatten.interval,
      na.rm = na.rm,
      ...
    )
  )

  #provide some default computed aesthetics
  default_computed_aesthetics = aes(ymin = conf.low, ymax = conf.high, size = -.prob)

  compute_aesthetics = l$compute_aesthetics
  l$compute_aesthetics = function(self, data, plot) {
    apply_default_computed_aesthetics(self, plot, default_computed_aesthetics)
    compute_aesthetics(data, plot)
  }

  map_statistic = l$map_statistic
  l$map_statistic = function(self, data, plot) {
    apply_default_computed_aesthetics(self, plot, default_computed_aesthetics)
    map_statistic(data, plot)
  }

  l
}

#' @importFrom grid grobTree
draw_key_pointinterval <- function(data, params, size) {
  grobTree(
    draw_key_path(transform(data, size = (3 + data$size) * 0.15), params, size)
  )
}

#' @rdname tidybayes-ggproto
#' @format NULL
#' @usage NULL
#' @importFrom grid grobName gTree gList
#' @import ggplot2
#' @export
GeomPointinterval <- ggproto("GeomPointinterval", Geom,
  default_aes = aes(colour = "black", size = 1.35, linetype = 1, shape = 19,
    fill = NA, alpha = NA, stroke = 1),

  draw_key = draw_key_pointinterval,

  required_aes = c("x", "y", "ymin", "ymax"),

  draw_panel = function(data, panel_scales, coord, fatten.point = 1.8, fatten.interval = 0.15) {
    if (is.null(data$x))
      return(GeomLinerange$draw_panel(transform(data, size = (3 + size) * fatten.interval), panel_scales, coord))

    ggname("geom_pointinterval",
      gTree(children = gList(
        GeomLinerange$draw_panel(transform(data, size = (3 + size) * fatten.interval), panel_scales, coord),
        GeomPoint$draw_panel(transform(data, size = fatten.point * (3 + size) * fatten.interval), panel_scales, coord)
      ))
    )
  }
)
